"""""
Ejercicio 3: Área de un Triángulo
Escribe un programa en Python que solicite al usuario que ingrese la base y la altura de un triángulo, y luego calcule y muestre el área del triángulo.
"""""

Base = float(input("Ingrese la Base del Triangulo: "))
Altura = float(input("Ingrese la Altura del Triangulo:"))

Area = Base * Altura / 2

print("El Area del Triangulo es: ", Area)